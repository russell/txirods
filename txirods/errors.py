const_to_int = {}
int_to_const = {}


class Constant(int):
    def __new__(cls, name, value, **kwargs):
        inst = super(Constant, cls).__new__(cls, value, **kwargs)
        inst._name = name
        global const_to_int, int_to_const
        const_to_int[name] = inst
        int_to_const[inst] = name

        return inst



